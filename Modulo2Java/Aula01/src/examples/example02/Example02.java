package examples.example02;

public class Example02 {
    public static void main(String[] args) {

        int numero = 0;

        if (numero > 0) {
            System.out.println("O valor é positivo");
        } else {
            if (numero < 0){
                System.out.println("O valor é negativo");
            } else {
                System.out.println("O valor é zero");
            }
        }
    }
}
